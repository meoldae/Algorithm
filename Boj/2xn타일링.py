import sys
input = sys.stdin.readline

n = int(input())

dp = [1]*(n+1) # n == 1일경우 dp[1] 에서 list Range Error 발생
dp[1] = 2

for i in range(2, n):
    dp[i] = (dp[i-1] + dp[i-2])%10007

print(dp[n-1])
